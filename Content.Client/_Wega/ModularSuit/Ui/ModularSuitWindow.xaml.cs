        {
            Text = partName,
            FontColorOverride = Color.FromHex("#ffffff"),
            HorizontalExpand = true,
            VerticalAlignment = VAlignment.Center
        };
        container.AddChild(nameLabel);

        AddChild(container);
    }
}
